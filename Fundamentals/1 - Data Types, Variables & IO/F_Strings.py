# f-string (formatted string) in Python is a string that is prefixed with an f or F and 
# allows you to insert expressions directly within the string, using curly braces {}.

# Basic Example
name = "Avi"
age = 25
print(f"Hello my name is {name}, I am {age} years old.")

# Math Expressions
x = 10
y = 20
print(f"The sum of {x} and {y} is {x+y}.")

# Formatting Numbers
pi = 3.1415926535
print(f"Pi rounded to 2 decimal places is {pi:.2f}")
